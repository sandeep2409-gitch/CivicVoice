export function getUserEmoji(identifier: string = ""): string {
  if (!identifier) return "👽";
  let hash = 0;
  for (let i = 0; i < identifier.length; i++) {
    hash = identifier.charCodeAt(i) + ((hash << 5) - hash);
  }
  hash = Math.abs(hash);
  return EMOJIS[hash % EMOJIS.length];
}
